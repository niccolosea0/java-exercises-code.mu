// Task N1: Find the sum of all integers from 1 to 100.

public class SumIntegers {
    public static void main(String[] args) {

        int sum = 0;
        for (int i = 1; i <= 100; i++) {
            sum += i;
        }

        System.out.println("Sum of all integers from 1 to 100: " + sum);
         
    }
}

